/*
  Example 1:
  Demonstrates basic TypeScript types:
  - number
  - string
  - boolean
  - bigint
  - symbol
*/

let age: number = 25;
let integer: number = 42;
let floating: number = 3.14;
let myname: string = "Partha";
let isStudent: boolean = false;
let token: symbol = Symbol("id");
const yyy = BigInt("123456789012345678901234567890");

console.log("age:", age);
console.log("integer:", integer);
console.log("floating:", floating);
console.log("myname:", myname);
console.log("isStudent:", isStudent);
console.log("token:", token);
console.log("yyy:", yyy);
